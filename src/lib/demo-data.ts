const CATEGORIES = ["Tops", "Bottoms", "Dresses", "Outerwear", "Footwear", "Accessories"];

const DESCRIPTIONS = [
  "White cotton t-shirt",
  "Black jeans",
  "Blue summer dress",
  "Red sweater",
  "Beige trench coat",
  "Navy blue blazer",
  "Gray hoodie",
  "Floral print skirt",
  "Brown leather boots",
  "White sneakers",
  "Denim jacket",
  "Black leather belt",
];

const TAGS = [
  "casual", "formal", "summer", "winter", "spring", "fall",
  "work", "weekend", "evening", "party", "beach", "workout"
];

export function generateDemoItems(): ClothingItemProps[] {
  return DEMO_IMAGES.map((imageUrl, index) => {
    const category = CATEGORIES[index % CATEGORIES.length];
    const description = DESCRIPTIONS[index % DESCRIPTIONS.length];
    const randomTags = Array.from({ length: 2 + Math.floor(Math.random() * 3) }, () => 
      TAGS[Math.floor(Math.random() * TAGS.length)]
    );
    
    return {
      id: uuidv4(),
      imageUrl,
      category,
      description,
      isFavorite: Math.random() > 0.7,
      tags: [...new Set(randomTags)], // Remove duplicates
    };
  });
}

export function generateInspirationItems(): ClothingItemProps[] {
  return INSPIRATION_IMAGES.map((imageUrl) => {
    const category = "Inspiration";
    
    return {
      id: uuidv4(),
      imageUrl,
      category,
      isFavorite: Math.random() > 0.7,
      tags: ["outfit", "inspiration"],
    };
  });
}
